    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed
       String sexo = (String)genero.getSelectedItem();
       Principal p= new Principal();
       
       if(nombre.getText().length()==0){
           JOptionPane.showMessageDialog(null, "Todos los campos con * son necesarios.");
       }else{
           paciente=new Pacientes();
           paciente.setCodigopostal(Integer.parseInt(codigop.getText()));
           paciente.setCorreoElectronico(correo.getText());
           paciente.setFechaNacimiento(fechaN.getCurrent().getTime());
           paciente.setGenero(sexo);
           paciente.setNombreCompleto(nombre.getText());
           paciente.setNumeroTelefono(numerotel.getText());
           paciente.setOcupacion(ocupacion.getText());
           conexionLocal();
           pc.create(paciente);
           JOptionPane.showMessageDialog(null, "Agregaodos");
           p.ActualizarPacientes();
           
       }
    }//GEN-LAST:event_RegistrarActionPerformed

    
